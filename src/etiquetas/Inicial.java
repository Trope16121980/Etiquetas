    public void insertarFoto() {
        try {
            if (jComboEmpresa.getSelectedItem().toString().equals("") || jComboEmpleado.getSelectedItem().toString().equals("")) {
                JOptionPane.showMessageDialog(null, "\nTendrá que rellenar los datos.");
            } else {
                File carpeta = new File(rutaFoto + "fotoCam\\" + jComboEmpresa.getSelectedItem().toString());
                File prueba = new File(carpeta + "\\" + jComboEmpleado.getSelectedItem().toString());
                if (!carpeta.exists()) {
                    JOptionPane.showMessageDialog(null, "\nLa empresa no existe.");
                } else if (!prueba.exists()) {
                    JOptionPane.showMessageDialog(null, "\nLa persona no existe.");
                } else if (prueba.exists()) {
                    BufferedImage bufferedImage = ImageIO.read(new File(carpeta + "\\" + jComboEmpleado.getSelectedItem().toString()));
                    Image image = bufferedImage.getScaledInstance(119,128, Image.SCALE_DEFAULT);
                    ImageIcon icon = new ImageIcon(image);
                    jlabelFoto.setIcon(icon);
                    int confirmado = JOptionPane.showOptionDialog(null,
                            "¿Quiere crear los datos en la etiqueta?",
                            "Datos etiqueta",
                            JOptionPane.YES_NO_OPTION,
                            JOptionPane.QUESTION_MESSAGE,
                            null, new Object[]{"Si", "No"}, "Si");
                    switch (confirmado) {
                        case JOptionPane.OK_OPTION:
                            String entradaUsuario = JOptionPane.showInputDialog("Escriba los datos de la etiqueta");
                            txtNomEmprFoto.setText(entradaUsuario);
                            break;
                        case JOptionPane.NO_OPTION:
                            if (txtFecha.getText().equals("")) {
                                Date fecha_actual = new Date();
                                SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
                                String nombre = jComboEmpleado.getSelectedItem().toString();
                                String nombreEtiqueta = nombre.substring(0, nombre.lastIndexOf("."));
                                JOptionPane.showMessageDialog(null, "Estos son los datos de la etiqueta:\n"
                                        + jComboEmpresa.getSelectedItem().toString() + " - " + nombreEtiqueta + " - " + formatoFecha.format(fecha_actual));
                                txtNomEmprFoto.setText(jComboEmpresa.getSelectedItem().toString() + " - " + nombreEtiqueta + " - " + formatoFecha.format(fecha_actual));
                            } else {
                                String nombre = jComboEmpleado.getSelectedItem().toString();
                                String nombreEtiqueta = nombre.substring(0, nombre.lastIndexOf("."));
                                JOptionPane.showMessageDialog(null, "Se escribiran los nombres de la 'Empresa - Trabajador - Fecha'");
                                txtNomEmprFoto.setText(jComboEmpresa.getSelectedItem().toString() + " - " + nombreEtiqueta + " - " + txtFecha.getText());
                            }
                    }
                }
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "\nError insertando datos.");
        }
    }

